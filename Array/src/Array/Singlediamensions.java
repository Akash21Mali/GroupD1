package Array;

public class Singlediamensions {
public static void main(String[] args) {
	int Ar[]= {10,40,50,30,20};//array size 5
	System.out.println();
	System.out.println(Ar[1]);
	for (int i = 0; i < Ar.length; i++) {
		System.out.println(Ar[i]);
	}
	System.out.println(Ar.length);

}
}
 